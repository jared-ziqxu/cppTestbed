#include <iostream>
using namespace std;

int main() {
	double value = 7/2;
	cout << "value: " << value << endl;
	double value1 = (double)7/2;
	cout << "value1: " << value1 << endl;
	double value2 = 7.0/2;
	cout << "value2: " << value2 << endl;
	int value3 = 12%5;
	cout << "value3: " << value3 << endl;
	return 0;
}
